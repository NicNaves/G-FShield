export const DEFAULT_LOCALE = "en-US";
export const SUPPORTED_LOCALES = Object.keys(translations);

const getValueByPath = (dictionary, key) =>
  String(key)
    .split(".")
    .reduce((value, pathPart) => (value && value[pathPart] !== undefined ? value[pathPart] : undefined), dictionary);

export const translate = (locale = DEFAULT_LOCALE, key, params = {}) => {
  const selectedLocale = SUPPORTED_LOCALES.includes(locale) ? locale : DEFAULT_LOCALE;
  const fallbackValue = getValueByPath(translations[DEFAULT_LOCALE], key);
  const rawValue = getValueByPath(translations[selectedLocale], key) ?? fallbackValue ?? key;

  if (typeof rawValue !== "string") {
    return rawValue;
  }

  return Object.entries(params).reduce((message, [paramKey, paramValue]) => {
    const safeValue = paramValue === undefined || paramValue === null ? "" : String(paramValue);
    return message.replaceAll(`{{${paramKey}}}`, safeValue);
  }, rawValue);
};

export default translations;
